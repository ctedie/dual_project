bool LED_CONTROL_Init(void)
{

	uint8_t i;
	//Init the buffer array
	for (i = 0; i < MAX_NB_MSG; ++i)
	{
		m_arrMsg[i].isUsed = false;
	}

	m_channelNumber = SerialLinkFrameProtocoleInit(SERIAL1,
												   B115200,
												   BIT_8,
												   PARITY_NONE,
												   STOP_BIT_1,
												   process_data,
												   &m_channelNumber,
												   AllocMsg,
												   FreeMsg
												   );



	return true;
}

